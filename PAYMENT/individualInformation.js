class individualInformation{
    constructor(
        name,
        surname,
        nickName,
        phoneNumber,
        email,
        site,
        dailyRate
    )
        {
        this.name = name;
        this.surname = surname;
        this.nickName = nickName;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.site = site;
        this.dailyRate = dailyRate;
        }
}

exports = individualInformation;